    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String enunciadoSQL;
        int nr;         //Número de registros 
        String claveMascota;
        String nombreMascota;
        String tipoMascota;
        int edad;
        String nombreDueño;
        limpiarTabla();
        Object[][] datos;

        claveMascota = txtClave.getText();
        enunciadoSQL = "SELECT * FROM mascotas WHERE claveMascota LIKE '%" + claveMascota + "%'";
        con.consultar(enunciadoSQL);
        nr = con.getNumRegistros();
        datos = new Object[nr][5];
        //tblDatos.setModel(new DefaultTableModel(datos));

        if (con.irPrimero()) {
            for (int i = 0; i < nr; i++) {
                claveMascota = con.obtenerCampo("claveMascota");
                nombreMascota = con.obtenerCampo("nombreMascota");
                tipoMascota = con.obtenerCampo("tipoMascota");
                edad = Integer.parseInt(con.obtenerCampo("edad"));
                nombreDueño = con.obtenerCampo("nombreDueño");
                tblDatos.setValueAt(claveMascota, i, 0);
                tblDatos.setValueAt(nombreMascota, i, 1);
                tblDatos.setValueAt(tipoMascota, i, 2);
                tblDatos.setValueAt(edad, i, 3);
                tblDatos.setValueAt(nombreDueño, i, 4);
                con.irSiguiente();
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

