    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            String enunciadoSQL;
            int pos;        //Posición del registro seleccionado para eliminar
            String claveMascota;
            int op;         //Opción de confirmación

            pos = tblDatos.getSelectedRow();
            if (pos != -1) {
                op = JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar "
                        + "está mascota");
                if (op == JOptionPane.OK_OPTION) {
                    claveMascota = tblDatos.getValueAt(pos, 0).toString();
                    enunciadoSQL = "DELETE FROM mascotas WHERE claveMascota = " + claveMascota + "";
                    con.actualizar(enunciadoSQL);
                    limpiarTabla();
                    //this.btnBuscarActionPerformed(null);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Selecciona un registro para eliminar");
                limpiarTabla();
            }
        } catch (java.lang.NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Selecciona un campo con información para eliminar el registro.");
        }
        tblDatosMouseClicked(null);
        /*btnEliminar.setEnabled(false);
        btnModificar.setEnabled(false);*/
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        // TODO add your handling code here:
        try {
            int pos;
            String claveMascota;

            pos = tblDatos.getSelectedRow();
            if (tblDatos.getValueAt(pos, 1) != null) {

                claveMascota = tblDatos.getValueAt(pos, 1).toString();
                txtClave.setText(claveMascota);
                btnEliminar.setEnabled(true);
            }
        } catch (java.lang.ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "¡Selecciona un campo de la tabla con datos!");
        }

    }//GEN-LAST:event_tblDatosMouseClicked

