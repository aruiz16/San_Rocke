    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        String enunciadoSQL;
        int nr;
        String[] datos;
        
        enunciadoSQL="SELECT * FROM animales";
        con.consultar(enunciadoSQL);
        
        nr = con.getNumRegistros();
        datos = new String[nr];
        
        if( con.irPrimero() ){
            for(int i=0;i<nr;i++){
                datos[i] = con.obtenerCampo("animal");
                con.irSiguiente();
            }
            cmbTipo.setModel(new DefaultComboBoxModel<>(datos));
        }
        String animal;
        
        animal = cmbTipo.getSelectedItem().toString();
        
        //LLenado del combo Modelos respecto a la marca
        enunciadoSQL = "SELECT * FROM razas WHERE animal = '"+animal+"'";
        con.consultar(enunciadoSQL);
        nr = con.getNumRegistros();
        datos = new String[nr];
        if( con.irPrimero() ){
            for(int i=0;i<nr;i++){
                datos[i] = con.obtenerCampo("nombreRaza");
                con.irSiguiente();
            }
            cmbRaza.setModel(new DefaultComboBoxModel<>(datos));
        }
    }//GEN-LAST:event_formWindowOpened

