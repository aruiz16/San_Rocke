    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String clave = txtClave.getText().toUpperCase().trim();
        String nomMascota = txtNombreMascota.getText().toUpperCase().trim();
        String tipoMascota = txtTipo.getText().toUpperCase().trim();
        String raza = txtRaza.getText().toUpperCase().trim();
        double peso = Double.parseDouble(txtPeso.getText());;
        int edad = Integer.parseInt(txtEdad.getText());;
        String nomDueño = txtNombreDueño.getText().toUpperCase().trim();
        
        String enunciadoSQL = "UPDATE mascotas SET nombreMascota='"
                +nomMascota+"',tipoMascota='"+tipoMascota+"',raza='"+raza+"',"
                + "peso="+peso+",edad="+edad+",nombreDueño='"+nomDueño+"' "
                + "Where claveMascota="+clave;
        
        txtNombreMascota.setEditable(false);
        txtTipo.setEditable(false);
        txtRaza.setEditable(false);
        txtEdad.setEditable(false);
        txtNombreDueño.setEditable(false);
        txtPeso.setEditable(false);
        con.actualizar(enunciadoSQL);
    }//GEN-LAST:event_btnAceptarActionPerformed

