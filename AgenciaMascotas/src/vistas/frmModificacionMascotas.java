    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String clave;
        String enunciadoSQL;
        
        clave = txtClave.getText().toUpperCase().trim();
        
        enunciadoSQL = "SELECT * FROM mascotas WHERE claveMascota='"+clave+"'";
        con.consultar(enunciadoSQL);
        if(con.irPrimero()){
            txtNombreMascota.setText(con.obtenerCampo("nombreMascota"));
            txtTipo.setText(con.obtenerCampo("tipoMascota"));
            txtRaza.setText(con.obtenerCampo("raza"));
            txtNombreDueño.setText(con.obtenerCampo("nombreDueño"));
            txtEdad.setText(con.obtenerCampo("edad"));
            txtPeso.setText(con.obtenerCampo("peso"));
       }else {
            JOptionPane.showMessageDialog(this, "No se pueden dar bajas en "
                    + "el sistema.");
        }
        txtNombreMascota.setEditable(true);
        txtEdad.setEditable(true);
        txtNombreDueño.setEditable(true);
        txtPeso.setEditable(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

